    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // UNIVERSITY SEARCH
        jTextArea6.setText("");
        String uniChoice = (String)jComboBox2.getSelectedItem();
        
        // display students w selected university
        for (int i = 0; i < Student.stuList.size(); i++) {
            for (int e = 0; e < Student.stuList.get(i).uniList.size(); e++) {
                if (Student.stuList.get(i).uniList.get(e).equals(uniChoice)) {
                    jTextArea6.setText(jTextArea6.getText() + Student.stuList.get(i).getUsName()+ "\n");
                }
            } 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

