    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        //  CANCEL MEETING
        //remove meeting
        currentC.getStu().meetList.remove(currentC);  
        currentC.getStu().cancelledMeet = currentC;
        Meeting.allMeet.remove(currentC);
        
        // update notes
        currentC.getStu().cancelledMeet.setNotes(jTextArea3.getText());
        
        clearMeetingList();
        renderMeetingList();
        Meeting.saveToFile();
        Student.saveToFile();
        cancelMeet.setVisible(false);
        this.setVisible(true);
    }//GEN-LAST:event_jButton10ActionPerformed

