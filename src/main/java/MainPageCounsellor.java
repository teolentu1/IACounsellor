    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // STUDENT SEARCH
        stuSearch.setVisible(true);
        
        // show all students in ComboBox
        jComboBox1.removeAllItems();
        for (int i = 0; i < Student.stuList.size(); i++) {
            jComboBox1.addItem(Student.stuList.get(i).getUsName());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // STUDENT SEARCH
        jTextArea5.setText("");
        
        // search for selected student
        Student current = null;
        for (int i = 0; i < Student.stuList.size(); i++) {
            if (jComboBox1.getSelectedItem().equals(Student.stuList.get(i).getUsName())) {
                current = Student.stuList.get(i);
            }
        }
        
        // display their meetings
        for (int i = 0; i < current.meetList.size(); i++ ) {
            jTextArea5.setText(jTextArea5.getText() + current.meetList.get(i).getDate() + " - " 
           + current.meetList.get(i).getTime() + "\n");
        }
    }//GEN-LAST:event_jButton11ActionPerformed

